static unsigned int simulated_device_number(void)
{
#if defined(CONFIG_BOARD_NRF54L15BSIM)
	return bsim_args_get_global_device_nbr();
#else
	return 0U;
#endif
}

const struct sap_device_credential *sap_credentials_select(enum sap_role role)
{
	size_t i;
	unsigned int dev_num = simulated_device_number();

	if (role == SAP_ROLE_CENTRAL) {
		return &central_credentials[0];
	}

	for (i = 0; i < ARRAY_SIZE(peripheral_credentials); i++) {
		if (peripheral_credentials[i].simulated_device_number == dev_num) {
			return &peripheral_credentials[i];
		}
	}

	return &peripheral_credentials[0];
}

const uint8_t *sap_credentials_ca_public_key(size_t *len)
{
	if (len != NULL) {
		*len = sizeof(sap_ca_public_key);
	}

	return sap_ca_public_key;
}
